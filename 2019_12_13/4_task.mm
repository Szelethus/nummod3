################################################################
# Includes.
################################################################

with(linalg):;
with(plots):;

################################################################
# Global option setup.
################################################################

interface(warnlevel=0);
plotsetup(maplet);

################################################################
# Function definitions.
################################################################

paramplot := proc(r);
	FirstDE := diff(x(t), t) = y(t);
	SecondDE := diff(y(t), t) = -x(t) + r * t;

	BVs := {x(0) = 0, y(0) = 0};
	DEs := {FirstDE, SecondDE};
	Result := dsolve(BVs union DEs, type=numeric);
	setcolors(["BlueViolet", "Coral"]);
	odeplot(Result, [[t, x(t)], [t, y(t)]], 0..4*Pi);
end;

################################################################
# Usage.
################################################################


paramplot(2);
################################################################



################################################################
# Usage.
################################################################

Alfa := 2/3;
Bheta := 4/3;
Gamma := 1;
Delta := 1;

FirstDE := diff(x(t), t) = Alfa * x(t) - Bheta * x(t) * y(t);
SecondDE := diff(y(t), t) = Delta * x(t) * y(t) - Gamma * y(t);

DEs := {FirstDE, SecondDE};
BVs := {x(0) = 1, y(0) = 1};

Result := dsolve(DEs union BVs, { x(t), y(t) }, type = numeric);

odeplot(Result, [[t, x(t)], [t, y(t)]], 0 .. 10);
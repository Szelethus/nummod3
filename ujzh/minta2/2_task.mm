with(linalg):;
with(plots):;

################################################################
# Global option setup.
################################################################

interface(warnlevel=0);
plotsetup(maplet);

################################################################
# Function definitions.
################################################################

gen_hilbert_mtx := proc(n);
  return hilbert(n);
end;

################################################################
# Usage.
################################################################

N := 3;

XPoints := [seq(J, J=1..N)];
YPoints := [seq(cond(gen_hilbert_mtx(J)), J=1..N)];

plot([seq([XPoints[J], YPoints[J]], J=1..N)], style='point');

